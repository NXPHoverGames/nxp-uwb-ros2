#define UCI_MTS_CMD 0x20
#define UCI_PARAM_DEVICE_CONFIG_ID 0x01
#define EXTENDED_DEVICE_CONFIG_ID 0xE4

#define UCI_EXT_PARAM_DPD_WAKEUP_SRC 0x02
#define UCI_EXT_PARAM_WTX_COUNT_CONFIG 0x03
#define UCI_EXT_PARAM_DPD_ENTRY_TIMEOUT 0x04
#define UCI_EXT_PARAM_TX_PULSE_SHAPE_CONFIG 0x28
#define UCI_EXT_PARAM_NXP_EXTENDED_NTF_CONFIG 0x33

int sr1xx_core_reset(sr1xx_dev *dev) {
    int ret;

    CoreDeviceResetCmd_view core_reset = {0};
    core_reset.reset_config = RESETCONFIG_UWBS_RESET;
    uint8_t buffer[uci_size_CoreDeviceResetCmd()];
    ret = uci_serialize_CoreDeviceResetCmd(&core_reset, buffer);

    ret = sr1xx_send_cmd(dev, buffer, ret, NULL, 2000);

    /* Technically we should wait on device_state: DEVICE_STATE_READY
     * But now simply timeout on 2000ms
     */

    return ret;
}

